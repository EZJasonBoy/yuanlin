package com.ninetowns.tootoopluse.bean;

public class AddressListBean {

	private String address_realName;
	
	private String address_phoneNumber;
	
	private String address_detail;
	
	private String address_postcode;
	/**送多少份**/
	private String address_memberCount;

	public String getAddress_realName() {
		return address_realName;
	}

	public void setAddress_realName(String address_realName) {
		this.address_realName = address_realName;
	}

	public String getAddress_phoneNumber() {
		return address_phoneNumber;
	}

	public void setAddress_phoneNumber(String address_phoneNumber) {
		this.address_phoneNumber = address_phoneNumber;
	}

	public String getAddress_detail() {
		return address_detail;
	}

	public void setAddress_detail(String address_detail) {
		this.address_detail = address_detail;
	}

	public String getAddress_postcode() {
		return address_postcode;
	}

	public void setAddress_postcode(String address_postcode) {
		this.address_postcode = address_postcode;
	}

	public String getAddress_memberCount() {
		return address_memberCount;
	}

	public void setAddress_memberCount(String address_memberCount) {
		this.address_memberCount = address_memberCount;
	}

	@Override
	public String toString() {
		return "AddressListBean [address_realName=" + address_realName
				+ ", address_phoneNumber=" + address_phoneNumber
				+ ", address_detail=" + address_detail + ", address_postcode="
				+ address_postcode + ", address_memberCount="
				+ address_memberCount + "]";
	}


	
}
