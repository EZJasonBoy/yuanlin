package com.ninetowns.tootoopluse.bean;

import java.io.Serializable;
import java.util.List;
/**
 * 
* @ClassName: ActivityStructure 
* @Description: 保存编辑活动的结构
* @author wuyulong
* @date 2015-3-10 下午2:16:54 
*
 */
public class ActivityStructure implements Serializable{
	private String StoryId;//每个故事的id
	private List<PageBean> PageList;//每个故事的结构集合
	public String getStoryId() {
		return StoryId;
	}
	public void setStoryId(String storyId) {
		StoryId = storyId;
	}
	public List<PageBean> getPageList() {
		return PageList;
	}
	public void setPageList(List<PageBean> pageList) {
		PageList = pageList;
	}
	@Override
	public String toString() {
		return "ActivityStructure [StoryId=" + StoryId + ", PageList="
				+ PageList + "]";
	}

}
