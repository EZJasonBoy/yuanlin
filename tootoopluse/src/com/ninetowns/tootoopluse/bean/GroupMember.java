package com.ninetowns.tootoopluse.bean;

/** 
* @ClassName: SortModel 
* @Description:群组成员实体类
* @author zhou
* @date 2015-3-3 下午4:09:07 
*  
*/
public class GroupMember {
	
	/** 
	* @Fields LogoUrl : 头像地址
	*/ 
	private String LogoUrl;
	
	private String UserId;
	
	private String UserName;   //显示的数据
	private String sortLetters;  //显示数据拼音的首字母
	/** 
	* @Fields GroupMemberId :群组成员id
	*/ 
	private String GroupMemberId; 
	
	
	/** 
	* @Fields BusinessStatus :是否为商户
	*/ 
	private String BusinessStatus;//1为是 
	/** 
	* @Fields IsAdmin : 是否为团长
	*/ 
	private String IsAdmin;//1为是
	public String getBusinessStatus() {
		return BusinessStatus;
	}
	public void setBusinessStatus(String businessStatus) {
		BusinessStatus = businessStatus;
	}
	public String getIsAdmin() {
		return IsAdmin;
	}
	public void setIsAdmin(String isAdmin) {
		IsAdmin = isAdmin;
	}
	@Override
	public String toString() {
		return "GroupMember [LogoUrl=" + LogoUrl + ", UserId=" + UserId
				+ ", UserName=" + UserName + ", sortLetters=" + sortLetters
				+ ", GroupMemberId=" + GroupMemberId + ", GroupId=" + GroupId
				+ ", UserGrade=" + UserGrade + "]";
	}
	public String getLogoUrl() {
		return LogoUrl;
	}
	public void setLogoUrl(String logoUrl) {
		LogoUrl = logoUrl;
	}
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public String getGroupMemberId() {
		return GroupMemberId;
	}
	public void setGroupMemberId(String groupMemberId) {
		GroupMemberId = groupMemberId;
	}
	public String getGroupId() {
		return GroupId;
	}
	public void setGroupId(String groupId) {
		GroupId = groupId;
	}
	public String getUserGrade() {
		return UserGrade;
	}
	public void setUserGrade(String userGrade) {
		UserGrade = userGrade;
	}
	/** 
	* @Fields GroupId : 群组id
	*/ 
	private String GroupId; 
	/** 
	* @Fields UserGrade : 用户等级
	*/ 
	private String UserGrade; 
	public String getUserId() {
		return UserId;
	}
	public void setUserId(String userId) {
		this.UserId = userId;
	}
	public String getSortLetters() {
		return sortLetters;
	}
	public void setSortLetters(String sortLetters) {
		this.sortLetters = sortLetters;
	}
}
