public class PriorityCodeBean {

	public String PriorityCodeId;//优先码Id
	public String DateUsed;//使用日期
	public String UserIdUsed;//使用人Id
	public String Status;//1未使用，2已经使用
	public String CreateDate;//开始日期
	public String UserNameUsed;//使用人名称
	public String UserGradeUsed;//使用人等级
	@Override
	public String toString() {
		return "PriorityCodeBean [PriorityCodeId=" + PriorityCodeId
				+ ", DateUsed=" + DateUsed + ", UserIdUsed=" + UserIdUsed
				+ ", Status=" + Status + ", CreateDate=" + CreateDate
				+ ", UserNameUsed=" + UserNameUsed + ", UserGradeUsed="
				+ UserGradeUsed + "]";
	}

}
