package com.ninetowns.tootooplus.bean;


public class MessageBean {
	
	private  String MessageId;
	private  String UserId;
	private  String UserName;
	/** 
	* @Fields LogoUrl : 头像
	*/ 
	private  String LogoUrl;
	private  String StoryImg;
	private  String StoryId;
	/** 
	* @Fields Content : 消息内容 
	*/ 
	private  String Content;
	/** 
	* @Fields Agree : 同意
	*/ 
	private  String Agree;
	private  long CreateDate;
	public String getMessageId() {
		return MessageId;
	}
	public void setMessageId(String messageId) {
		MessageId = messageId;
	}
	public String getUserId() {
		return UserId;
	}
	public void setUserId(String userId) {
		UserId = userId;
	}
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public String getLogoUrl() {
		return LogoUrl;
	}
	public void setLogoUrl(String logoUrl) {
		LogoUrl = logoUrl;
	}
	public String getStoryImg() {
		return StoryImg;
	}
	public void setStoryImg(String storyImg) {
		StoryImg = storyImg;
	}
	public String getStoryId() {
		return StoryId;
	}
	public void setStoryId(String storyId) {
		StoryId = storyId;
	}
	public String getContent() {
		return Content;
	}
	public void setContent(String content) {
		Content = content;
	}
	public String getAgree() {
		return Agree;
	}
	public void setAgree(String agree) {
		Agree = agree;
	}
	public long getCreateDate() {
		return CreateDate;
	}
	public void setCreateDate(long createDate) {
		CreateDate = createDate;
	}
	public MessageBean(String messageId, String userId, String userName,
			String logoUrl, String storyImg, String storyId, String content,
			String agree, long createDate) {
		super();
		MessageId = messageId;
		UserId = userId;
		UserName = userName;
		LogoUrl = logoUrl;
		StoryImg = storyImg;
		StoryId = storyId;
		Content = content;
		Agree = agree;
		CreateDate = createDate;
	}
	public MessageBean() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "MessageBean [MessageId=" + MessageId + ", UserId=" + UserId
				+ ", UserName=" + UserName + ", LogoUrl=" + LogoUrl
				+ ", StoryImg=" + StoryImg + ", StoryId=" + StoryId
				+ ", Content=" + Content + ", Agree=" + Agree + ", CreateDate="
				+ CreateDate + "]";
	}
	

}
